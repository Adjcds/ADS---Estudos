# Define uma tupla de linguagens de programação
linguagens = ("python", "js", "c", "java", "csharp",)

# Encontra o índice do elemento "java" na tupla e imprime
print(linguagens.index("java"))  # 3

# Encontra o índice do elemento "python" na tupla e imprime
print(linguagens.index("python"))  # 0

# O método `index()` é usado para encontrar o índice de um determinado elemento na tupla. Ele retorna o índice da primeira ocorrência do elemento especificado. Neste exemplo, "java" está na posição 3 da tupla `linguagens`, e "python" está na posição 0.