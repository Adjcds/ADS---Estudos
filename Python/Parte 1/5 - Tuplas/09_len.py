# Define uma tupla de linguagens de programação
linguagens = (
    "python",
    "js",
    "c",
    "java",
    "csharp",
)

# Imprime o número de elementos na tupla
print(len(linguagens))  # 5

#  função len() é usada para retornar o número de elementos na tupla. Neste caso, a tupla linguagens possui 5 elementos, então len(linguagens) retorna 5.