# Define uma tupla de cores
cores = (
    "vermelho",
    "azul",
    "verde",
    "azul",
)

# Conta o número de ocorrências da cor "vermelho" na tupla e imprime
print(cores.count("vermelho"))  # 1

# Conta o número de ocorrências da cor "azul" na tupla e imprime
print(cores.count("azul"))  # 2

# Conta o número de ocorrências da cor "verde" na tupla e imprime
print(cores.count("verde"))  # 1

# O método count() é usado para contar quantas vezes um determinado elemento aparece na tupla. Neste exemplo, "vermelho" aparece uma vez, "azul" aparece duas vezes e "verde" aparece uma vez na tupla cores.