# Criação de um dicionário com dados iniciais
dados = {"nome": "Guilherme", "idade": 28, "telefone": "3333-1234"}

# Impressão dos valores associados a cada chave
print(dados["nome"])  # Saída: "Guilherme"
print(dados["idade"])  # Saída: 28
print(dados["telefone"])  # Saída: "3333-1234"

# Atualização dos valores das chaves no dicionário
dados["nome"] = "Maria"
dados["idade"] = 18
dados["telefone"] = "9988-1781"

# Impressão do dicionário atualizado
print(dados)  # Saída: {"nome": "Maria", "idade": 18, "telefone": "9988-1781"}

# Resumo:
# Este código demonstra como acessar e modificar os valores em um dicionário Python.
# Primeiro, cria um dicionário com chaves e valores iniciais.
# Em seguida, imprime os valores associados a cada chave.
# Depois, atualiza os valores das chaves existentes no dicionário.
# Finalmente, imprime o dicionário atualizado, mostrando que os valores foram modificados com sucesso.
