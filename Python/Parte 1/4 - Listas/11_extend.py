# Define uma lista inicial de linguagens
linguagens = ["python", "js", "c"]

# Imprime a lista inicial
print(linguagens)  # ["python", "js", "c"]

# Usa o método extend() para adicionar múltiplos elementos à lista
linguagens.extend(["java", "csharp"])

# Imprime a lista após a extensão
print(linguagens)  # ["python", "js", "c", "java", "csharp"]

# linguagens.extend(["java", "csharp"]): Adiciona os elementos da lista ["java", "csharp"] 
# à lista linguagens, estendendo-a com os elementos adicionados.