# Define uma lista de frutas
frutas = ["maçã", "laranja", "uva", "pera"]

# Imprime o primeiro elemento da lista (índice 0)
print(frutas[0])  # Output: maçã

# Imprime o terceiro elemento da lista (índice 2)
print(frutas[2])  # Output: uva

# Em Python, os elementos de uma lista são indexados numericamente, começando do índice 0. 
# Portanto, frutas[0] retorna o primeiro elemento da lista frutas (que é "maçã"), e frutas[2] retorna o terceiro elemento da lista (que é "uva").