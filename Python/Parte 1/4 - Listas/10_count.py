# Define uma lista de cores, com repetições
cores = ["vermelho", "azul", "verde", "azul"]

# Conta o número de ocorrências da cor "vermelho" na lista
print(cores.count("vermelho"))  # 1

# Conta o número de ocorrências da cor "azul" na lista
print(cores.count("azul"))  # 2

# Conta o número de ocorrências da cor "verde" na lista
print(cores.count("verde"))  # 1

# cores.count("cor"): Retorna o número de ocorrências do elemento "cor" na lista cores.
# Neste caso, conta o número de vezes que as cores "vermelho", "azul" e "verde" aparecem na lista.