# Define uma lista de frutas com quatro elementos: maçã, laranja, uva e pera
frutas = ["maçã", "laranja", "uva", "pera"]

# Imprime o último elemento da lista frutas, que é "pera", utilizando indexação negativa
print(frutas[-1])  # pera

# Imprime o terceiro elemento a contar do fim da lista frutas, que é "laranja", utilizando indexação negativa
print(frutas[-3])  # laranja

# Em Python, a indexação negativa permite acessar elementos de uma lista a partir do final. 
# O índice -1 refere-se ao último elemento, -2 ao penúltimo, e assim por diante.
# Portanto, frutas[-1] retorna o último elemento da lista frutas (que é "pera"), e frutas[-3]
# retorna o terceiro elemento a contar do fim (que é "laranja").