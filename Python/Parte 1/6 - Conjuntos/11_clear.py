# Define um conjunto inicialmente contendo os números 1 e 23
sorteio = {1, 23}

# Imprime o conjunto antes de limpar
print(sorteio)  # Output: {1, 23}

# Limpa o conjunto
sorteio.clear()

# Imprime o conjunto após limpar (conjunto vazio)
print(sorteio)  # Output: {}

# O método clear() é usado para remover todos os elementos de um conjunto, deixando-o vazio.
#  Portanto, após chamar sorteio.clear(), o conjunto sorteio ficará vazio e print(sorteio) resultará em {}.