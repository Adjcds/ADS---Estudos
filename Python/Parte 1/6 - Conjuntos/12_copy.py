# Define um conjunto inicialmente contendo os números 1 e 23
sorteio = {1, 23}

# Imprime o conjunto antes de criar uma cópia
print(sorteio)  # Output: {1, 23}

# Cria uma cópia do conjunto usando o método copy()
copia_sorteio = sorteio.copy()

# Imprime a cópia do conjunto
print(copia_sorteio)  # Output: {1, 23}

# Neste exemplo, a função copy() é usada para criar uma cópia do conjunto sorteio e armazená-la
# na variável copia_sorteio. Então, a cópia do conjunto é impressa, e a cópia é 
# independente do conjunto original sorteio.