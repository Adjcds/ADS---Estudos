# Define dois conjuntos
conjunto_a = {1, 2}
conjunto_b = {3, 4}

# Realiza a união dos conjuntos e armazena o resultado em 'resultado'
resultado = conjunto_a.union(conjunto_b)

# Imprime o resultado da união
print(resultado)  # Output: {1, 2, 3, 4}

# O método union() retorna um novo conjunto que contém todos os elementos únicos de ambos os conjuntos originais. 
# Neste caso, o conjunto resultante resultado conterá os elementos {1, 2, 3, 4}.