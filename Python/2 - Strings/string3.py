# Define uma string com o nome completo "Guilherme Arthur de Carvalho"
nome = "Guilherme Arthur de Carvalho"

# Imprime o primeiro caractere da string 'nome'
print(nome[0])

# Imprime o penúltimo caractere da string 'nome'
print(nome[-2])

# Imprime os primeiros nove caracteres da string 'nome'
print(nome[:9])

# Imprime os caracteres da string 'nome' a partir do décimo caractere até o final
print(nome[10:])

# Imprime os caracteres da string 'nome' do décimo ao décimo quinto (exclusive)
print(nome[10:16])

# Imprime os caracteres da string 'nome' do décimo ao décimo quinto (exclusive), pulando de dois em dois
print(nome[10:16:2])

# Imprime toda a string 'nome'
print(nome[:])

# Imprime a string 'nome' invertida
print(nome[::-1])
