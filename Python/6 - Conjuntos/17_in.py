# Define um conjunto de números com elementos duplicados
numeros = {1, 2, 3, 1, 2, 4, 5, 5, 6, 7, 8, 9, 0}

# Verifica se o número 1 está presente no conjunto
print(1 in numeros)  # Output: True

# Verifica se o número 10 está presente no conjunto
print(10 in numeros)  # Output: False

# O operador in é usado para verificar se um determinado elemento está presente
# em uma coleção, como uma lista, tupla, conjunto, dicionário, etc. 
# No caso do conjunto numeros, 1 in numeros retorna True porque o número 1 está presente no conjunto,
# enquanto 10 in numeros retorna False porque o número 10 não está presente no conjunto.
