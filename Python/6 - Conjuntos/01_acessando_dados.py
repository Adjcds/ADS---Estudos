# Define um conjunto de números com elementos duplicados
numeros = {1, 2, 3, 2}

# Converte o conjunto em uma lista
numeros = list(numeros)

# Imprime o primeiro elemento da lista
print(numeros[0])

# Quando um conjunto é convertido em uma lista, os elementos duplicados são removidos e a ordem dos elementos
# é preservada. Portanto, o primeiro elemento da lista numeros após a conversão é o primeiro elemento
# único do conjunto, que neste caso é 1.