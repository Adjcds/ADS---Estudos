## Operador de Associação

# O operador de associação `in` é usado para verificar se um valor está presente em uma sequência (como uma lista, tupla, conjunto ou string).

## Exemplo:

frutas = ["limao", "uva"]
curso = "Curso de python"

print("laranja" not in frutas)   # True, "laranja" não está na lista de frutas
print("limao" in frutas)         # True, "limao" está na lista de frutas
print("Python" in curso)         # False, "Python" não está na string "Curso de python"

