# Adiciona uma lista de frutas contendo laranja, maçã e uva
frutas = ["laranja", "maçã", "uva"]
print(frutas)

# Reatribui a variável frutas para uma lista vazia
frutas = []
print(frutas)

# Converte a string "python" em uma lista de caracteres
letras = list("python")
print(letras)

# Cria uma lista de números de 0 a 9 usando a função range e a converte em uma lista
numeros = list(range(10))
print(numeros)

# Cria uma lista contendo informações sobre um carro
carro = ["Ferrari", "F8", 4200000, 2020, 2900, "São Paulo", True]
print(carro)

# A primeira linha cria uma lista frutas contendo os elementos "laranja", "maçã" e "uva".
# Na segunda linha, a variável frutas é reatribuída a uma lista vazia.
# Na terceira linha, a string "python" é convertida em uma lista de caracteres letras.
# Na quarta linha, a função range(10) cria um iterável de números de 0 a 9, que é então convertido em uma lista numeros.
# Na última linha, uma lista carro é criada contendo informações sobre um carro, como marca, modelo, preço, ano, etc.