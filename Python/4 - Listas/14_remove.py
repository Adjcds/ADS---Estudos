# Define uma lista de linguagens de programação
linguagens = ["python", "js", "c", "java", "csharp"]

# Remove o primeiro item "c" encontrado na lista
linguagens.remove("c")

# Imprime a lista resultante
print(linguagens)  # ["python", "js", "java", "csharp"]

# linguagens.remove("c"): Remove a primeira ocorrência do elemento "c" da lista linguagens.