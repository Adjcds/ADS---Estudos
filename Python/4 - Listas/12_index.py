linguagens = ["python", "js", "c", "java", "csharp"]

# Imprime o índice do elemento "java" na lista
print(linguagens.index("java"))  # Output: 3

# Imprime o índice do elemento "python" na lista
print(linguagens.index("python"))  # Output: 0

# linguagens.index("elemento"): Retorna o índice da
# primeira ocorrência do elemento especificado na lista. 
#Se o elemento não estiver na lista, gera um erro ValueError. 
# Neste caso, "java" está na posição 3 e "python" está na posição 0 da lista linguagens.