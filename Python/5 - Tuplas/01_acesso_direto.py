# Define uma tupla de frutas
frutas = ("maçã", "laranja", "uva", "pera",)

# Acessa o primeiro elemento da tupla (índice 0) e imprime
print(frutas[0])  # maçã

# Acessa o terceiro elemento da tupla (índice 2) e imprime
print(frutas[2])  # uva

# Em Python, os elementos de uma tupla são indexados numericamente, começando do índice 0. 
# Portanto, frutas[0] retorna o primeiro elemento da tupla frutas 
# (que é "maçã"), e frutas[2] retorna o terceiro elemento (que é "uva").